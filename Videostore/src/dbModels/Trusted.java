package dbModels;

import databaseOps.IDbModel;

public class Trusted implements IDbModel{
	public int cidFrom;
	public int cidTo;
	public String IsTrusted;
	
	public String toString(){
		return "From customer: " + cidFrom + "\n" +
				"To customer: " + cidTo + "\n" + 
				"Is trusted: " + IsTrusted + "\n\n";
	}
}
